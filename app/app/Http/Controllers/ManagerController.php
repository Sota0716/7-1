     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {   
        // 昇順
        $report= Violation_report::select('post_id')
        ->with('post.user')
        ->selectRaw('COUNT(post_id) as count_postid')
        ->groupBy('post_id')
        ->orderBy('count_postid', 'desc')
        ->distinct()
        ->take(20)
        ->get();
        return view('manager/manager_post',[
            'reports'=>$report,
        ]);
    }

